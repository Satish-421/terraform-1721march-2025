package main

import "fmt"

func main() {

	var length int
	var width int

	fmt.Print("\nEnter the length of the rectangle : ")
	fmt.Scanf("%d\n", &length)

	fmt.Print("\nEnter the width of the rectangle : ")
	fmt.Scanf("%d\n", &width)

	fmt.Println("Value of length is ", length)
	fmt.Println("Value of width is ", width)
}
